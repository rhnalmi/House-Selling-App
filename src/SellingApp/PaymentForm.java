    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        if(TotalPriceTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Incomplete Selection");
        } else if(DPTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "DP Must be Filled!");
        } else if(InstallmentPeriodTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Fill in the desired installment amount");
        } else{
            long landPrice = Long.parseLong(PriceLandTxt.getText()),
                    buildingPrice = Long.parseLong(BuildingPriceTxt.getText()),
                    availableLand = Long.parseLong(AvailableLandTxt.getText()),
                    dp = Long.parseLong(DPTxt.getText()),
                    installmentPeriod = Long.parseLong(InstallmentPeriodTxt.getText()),
                    totalPrice, Tax, Installment;
            totalPrice = (availableLand * landPrice) + buildingPrice;
            Tax = totalPrice * 10 / 100;
            Installment = (totalPrice + Tax) / installmentPeriod;
            TaxTxt.setText("" + Tax);
            InstallmentTxt.setText("" + Installment);
            
            
        }
    }//GEN-LAST:event_btnProcessActionPerformed

