    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(AgreementCbx.isSelected() == false){
            JOptionPane.showMessageDialog(null, "Data is still empty, Fill it first!","error",JOptionPane.ERROR_MESSAGE);
        } else{
            String typeHouse;
            if (rbT36.isSelected()){
                typeHouse = "Type 36";
            } else if (rbT45.isSelected()){
                typeHouse = "Type 45";
            } else {
                typeHouse = "Type 54";
            }
            
            try {
                String sql = "INSERT INTO house (name, area, type, wide, price, installmentAmount, InstallmentPeriiod) VALUES('"
                        + CustNameTxt.getText() + "','"
                        + cmbArea.getSelectedItem() + "','"
                        + typeHouse + "','"
                        + OriLandAreaTxt.getText() + "','"
                        + TotalPriceTxt.getText() + "','"
                        + InstallmentPeriodTxt.getText() + "','"
                        + InstallmentTxt.getText() + "')";
                Connection conn = (Connection)ConnectionDb.connectDatabase();
                PreparedStatement pst = (PreparedStatement) conn.prepareStatement(sql);
                pst.execute();
            } catch (SQLException ex) {
                Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            OptionMenu optionMenu = new OptionMenu();
            optionMenu.setVisible(true);
            this.getDesktopPane().add(optionMenu);
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed


